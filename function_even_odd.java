public class function_even_odd 
{
     static void even_odd(int n)
    {
        if(n%2==0){
            System.out.println(n + " is even number");
        }
        else{
            System.out.println(n+" is odd number");
        }
    }
    public static void main(String[] args)
    {
        even_odd(7);
    }
    

    
}
